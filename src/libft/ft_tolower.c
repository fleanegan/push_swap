#include "libft.h"

static int	ft_isupper(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (1);
	return (0);
}

int	ft_tolower(int input)
{
	if (ft_isupper(input))
		return (input - ('A' - 'a'));
	return (input);
}
