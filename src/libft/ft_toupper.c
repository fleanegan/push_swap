#include "libft.h"

static int	ft_islower(int c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	return (0);
}

int	ft_toupper(int input)
{
	if (ft_islower(input))
		return (input + ('A' - 'a'));
	return (input);
}
